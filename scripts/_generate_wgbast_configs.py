def generate_config(short_name: str, spec: dict) -> None:
    with open(TEMPLATE_CFG, "r", encoding="utf-8") as f:
        cfg = yaml.safe_load(f)

    # 1. simulation block
    cfg["simulation"]["start_date"] = "2011-04-01"
    cfg["simulation"]["end_date"] = "2016-03-31"
    cfg["simulation"]["seed"] = 42
    cfg["simulation"]["population_file"] = f"{spec['stem']}-InitialPopulations.csv"
    cfg["simulation"]["adult_arrival_file"] = f"{spec['stem']}-AdultArrivals.csv"
    # Arc L: WGBAST M74 forcing (fires only on rivers in the CSV —
    # Simojoki + Tornionjoki; Byske/Mörrum are no-ops)
    cfg["simulation"]["m74_forcing_csv"] = "data/wgbast/m74_ysfm_series.csv"

    # 2. light.latitude
    cfg["light"]["latitude"] = spec["latitude"]

    # 3. species-level smolt_min_length (WGBAST Skoglund 2024 latitudinal)
    for sp_name, sp_cfg in cfg.get("species", {}).items():
        sp_cfg["smolt_min_length"] = spec["smolt_min_length"]
    if "marine" in cfg:
        cfg["marine"]["smolt_min_length"] = spec["smolt_min_length"]

    # 4. Inject PSPC + river_name into each reach (keeping Nemunas-basin
    #    names and all other parameters verbatim from template).
    for reach_name, reach_cfg in cfg.get("reaches", {}).items():
        pspc = spec["pspc_by_reach"].get(reach_name)
        if pspc is None:
            # Remove any existing PSPC (non-natal for this river)
            reach_cfg.pop("pspc_smolts_per_year", None)
        else:
            reach_cfg["pspc_smolts_per_year"] = pspc
        reach_cfg["river_name"] = spec["river_name"]

    # Write
    out_path = ROOT / "configs" / f"example_{short_name}.yaml"
    total_pspc = sum(p for p in spec["pspc_by_reach"].values() if p)
    header = (
        f"# {spec['river_name']} WGBAST-assessment-ready fixture.\n"
        f"# Generated via scripts/_generate_wgbast_configs.py from example_baltic.yaml\n"
        f"# Latitude: {spec['latitude']}°N; smolt_min_length: {spec['smolt_min_length']} cm\n"
        f"# Fixture: tests/fixtures/{short_name}/\n"
        f"# Plan: docs/superpowers/plans/2026-04-20-arc-M-to-Q-expanded.md Arc M\n"
        f"#\n"
        f"# WGBAST PSPC total: {total_pspc:,} smolts/yr (WGBAST 2026 §3)\n"
        f"# Pragmatic scaffolding: reach NAMES are Nemunas-basin (from template\n"
        f"# shapefile) but each reach carries `river_name: {spec['river_name']}`\n"
        f"# and WGBAST PSPC. Temperature + flow forcing in the per-reach\n"
        f"# TimeSeriesInputs.csv files is latitude-appropriate for {spec['river_name']}.\n"
    )
    with open(out_path, "w", encoding="utf-8") as f:
        f.write(header)
        yaml.safe_dump(cfg, f, sort_keys=False, default_flow_style=False)
    print(f"[{short_name}] wrote {out_path}")


def main() -> None:
    for short_name, spec in RIVER_SPECS.items():
        generate_config(short_name, spec)


if __name__ == "__main__":
    main()
