"""Generate configs/example_{tornionjoki,simojoki,byskealven,morrumsan}.yaml
from the example_baltic.yaml template with per-river WGBAST overrides.

Pragmatic choice (Arc M): reuse the Nemunas-basin fixture shapefile and
reach names verbatim — they're just geometric scaffolding for the IBM
scheduler. What makes each config a WGBAST-river is (a) the simulation
temperature + flow forcing in the per-reach TimeSeriesInputs CSVs
(pre-generated by `_scaffold_wgbast_rivers.py`), (b) the WGBAST-specific
parameters injected into the reaches: `river_name`, `pspc_smolts_per_year`,
and species-level `smolt_min_length`.

This means reach NAMES in the YAML stay Nemunas/Atmata/Minija/... but
their `river_name` attribute (Arc L) and PSPC values (Arc K) are
WGBAST-calibrated. Later refinement (Arc M.4 / Arc 0) can rewrite the
DBF attributes to use real Torne/Simo reach names.

See docs/superpowers/plans/2026-04-20-arc-M-to-Q-expanded.md Arc M.
"""
